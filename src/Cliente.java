
public class Cliente {
	private long ID;
	private String nombre;
	private String apellido;
	private String email;
	private String password;
	
	
	public Cliente(long ID, String nombre, String apellido, String email, String email, String password)
	{
		this.ID = ID;
		this.nombre = nombre;
		this.apellido = apellido;
		this.email = email;
		this.password = password;
	}
}
